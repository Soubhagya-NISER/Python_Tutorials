def kg_to_g(kg):
    #Convert kilograms to grams.
    return kg * 1000

def g_to_kg(g):
    #Convert grams to kilograms.
    return g / 1000

def kg_to_lbs(kg):
    #Convert kilograms to pounds.
    return kg * 0.45

def lbs_to_kg(lbs):
    #Convert pounds to kilograms.
    return lbs / 0.45